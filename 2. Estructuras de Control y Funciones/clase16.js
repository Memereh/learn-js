var signo = prompt('Cual es tu signo?')

switch (signo) {
    case 'aries':
        console.log('Prepárese, ya que dará comienzo a un proceso constructivo en lo profesional. No permita que su timidez lo paralice al mostrar su ideología en público.');
        break;
    case 'tauro':
        console.log('No debería permitir que los miedos por frustraciones pasadas le obstaculicen el impulso creativo. Evite detenerse en este momento, en poco tiempo hallará la solución a ese problema.')
        break;
    case 'geminis':
    case 'géminis':
        console.log('Comience a aceptar y superar los conflictos vividos en el pasado. Aprenda a contener las emociones y no sea prisionero de ellas, caso contrario, no avanzará en su vida.')
        break;
    case 'cancer':
        console.log('Durante esta jornada se enfrentará a un desafío muy importante donde pondrá a prueba su vocación y compromiso profesional. Escoja el camino correcto.')
        break;
    case 'leo':
        console.log('Recuerde que antes de preocuparse por tonterías será mejor que actúe. Busque liberarse de los obstáculos que lo angustian en su vida personal.')
        break;
    case 'virgo':
        console.log('Le será de gran ayuda para estar equilibrado en su vida exteriorizar cada uno de sus sentimientos y las ideas que tiene guardadas. Evite desconectarse de las preocupaciones.')
        break;
    case 'libra':
        console.log('Aproveche el momento para revalorizar su autoestima y comenzar a desplegar las virtudes con las que cuenta en la vida. Busque algún proyecto que lo motive.')
        break;
    case 'escorpio':
        console.log('Momento para que intente cambiar la actitud y empiece a controlar su temperamento. Sepa que nadie de su entorno soportará más sus quejas continúas.')
        break;
    case 'sagitario':
        console.log('Posiblemente, se enfrentará a muchos obstáculos que le dificulten sus objetivos. No se impaciente, ya que usted estará fuerte y estable en sus acciones.')
        break;
    case 'capricornio':
        console.log('La confianza en usted mismo le permitirá esquivar las dificultades sin demasiado esfuerzo. Continúe encarando las situaciones del modo que lo está haciendo.')
        break;
    case 'acuario':
        console.log('Evite tener miedo a caer y volver a empezar por más que sus esfuerzos se vean limitados para obtener nuevos intereses. Muchas veces de los errores aprendemos cosas nuevas.')
        break;
    case 'piscis':
        console.log('Seguramente, se sentirá mucho mejor si se escucha. Intente preguntarse qué es lo que necesita y quiere para su vida en estos momentos.')
        break;
    default:
        console.log('No es un signo sodiacal valido.')
        break
}



